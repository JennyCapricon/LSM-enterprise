export const formatWhatsAppNumber = (phone) => {
  if (!phone) return '';
  let cleaned = phone.replace(/[^0-9]/g, '');
  if (cleaned.startsWith('0')) {
    cleaned = '234' + cleaned.slice(1);
  } else if (cleaned.startsWith('+')) {
    cleaned = cleaned.slice(1);
  }
  return cleaned;
};

export const generateVendorOrderMessage = (vendor, items, customer, orderRef) => {
  const itemLines = items.map((item, i) =>
    `${i + 1}. ${item.name}\n   Quantity: ${item.quantity}\n   Price: ₦${(item.price * item.quantity).toLocaleString()}`
  ).join('\n\n');

  const subtotal = items.reduce((sum, item) => sum + item.price * item.quantity, 0);

  return encodeURIComponent(
    `Hello ${vendor.businessName || vendor.name},\n\n` +
    `I want to order the following fabrics:\n\n` +
    `${itemLines}\n\n` +
    `Subtotal: ₦${subtotal.toLocaleString()}\n\n` +
    `Customer Name: ${customer?.name || 'N/A'}\n` +
    `Phone: ${customer?.phone || 'N/A'}\n` +
    `Order Reference: ${orderRef || 'N/A'}\n\n` +
    `Please confirm product availability and payment details.`
  );
};

